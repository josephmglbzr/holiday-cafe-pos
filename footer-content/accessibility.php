    <h2>Our Accessibility Policy</h2>
    <br>
    <p>At Holiday Cafe and Souvenir Shop, we are committed to ensuring that our online ordering platform is accessible to all users, including those with disabilities. We strive to provide an inclusive and user-friendly experience for everyone, regardless of their abilities. To fulfill this commitment, we have established the following accessibility policy:</p>
    <br>
    <h3>1. Compliance with Web Accessibility Standards:</h3>
    <br>
    <ul>
        <li>We adhere to the Web Content Accessibility Guidelines (WCAG) 2.1 Level AA standards to ensure our website is accessible to individuals with disabilities.</li>
        <li>Our development and design processes incorporate accessibility principles from the initial stages to ensure that accessibility is considered in every aspect of our website.</li>
    </ul>
    <br>
    <h3>2. Assistive Technologies Compatibility:</h3>
    <br>
    <ul>
        <li>We aim to ensure compatibility with commonly used assistive technologies such as screen readers, magnifiers, and voice recognition software.</li>
        <li>Our website is designed to be navigable and usable via keyboard-only navigation, without relying on a mouse.</li>
    </ul>
    <br>
    <h3>3. Content Accessibility:</h3>
    <br>
    <ul>
        <li>All images on our website are accompanied by descriptive alternative text, providing meaningful information to users who may not be able to see them.</li>
        <li>Videos on our platform include captions or transcripts to make them accessible to users who are deaf or hard of hearing.</li>
    </ul>
    <br>
    <h3>4. User Interface Accessibility:</h3>
    <br>
    <ul>
        <li>Interactive elements such as links, buttons, and forms have clear and visible focus states to facilitate keyboard navigation.</li>
        <li>Color contrast ratios are carefully considered to ensure that text and interactive elements are distinguishable and readable for users with low vision.</li>
    </ul>
    <br>
    <h3>5. Continuous Improvement:</h3>
    <br>
    <ul>
        <li>We are committed to continuously improving the accessibility of our website by conducting regular accessibility audits and implementing necessary enhancements.</li>
        <li>Feedback from users with disabilities is welcomed and valued. We encourage users to reach out to us with any accessibility-related concerns or suggestions for improvement.</li>
    </ul>
    <br>
    <h3>6. Training and Awareness:</h3>
    <br>
    <ul>
        <li>Our team members, including developers, designers, and content creators, receive training on web accessibility best practices to ensure ongoing awareness and understanding of accessibility requirements.</li>
        <li>We are dedicated to fostering a culture of accessibility within our organization and among our stakeholders.</li>
    </ul>
    <br>
    <h3>7. Accessibility Statement:</h3>
    <br>
    <ul>
        <li>We provide an accessibility statement on our website, outlining our commitment to accessibility and providing contact information for users to report accessibility issues.</li>
    </ul>
    <br>
    <h4>Contact Information:</h4>
    <p>If you encounter any accessibility barriers on our website or have suggestions for improvement, please contact us at holidaycafesouvenir@gmail.com.</p>
